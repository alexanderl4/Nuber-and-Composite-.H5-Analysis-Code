def run_neuber_analysis(df_calc):
    """ Executes the Neuber correction logic on the compiled data """
    res = {'stress':[], 'status':[], 'reduct':[], 'e_elas':[], 'e_plas':[], 'e_tot':[], 'mos':[]}
    
    # Iterate through stress and dynamic material properties row by row
    data_iterator = zip(df_calc['Linear_VonMises_Max'], df_calc['E'], df_calc['Fty'], df_calc['Ftu'], df_calc['n'])
    
    # Loop with TQDM progress bar
    for s_lin, E_mod, Fty, Ftu, n_exp in tqdm(data_iterator, total=len(df_calc), desc="  -> Crunching Neuber Math", unit="elem"):
        # Skip invalid or negative stress data
        if pd.isna(s_lin) or s_lin <= 0:
            for k in res: res[k].append(0 if k != 'status' else "No Data")
            continue
        
        is_plastic = s_lin > Fty # Check if stress exceeds Yield Strength
        
        if is_plastic:
            # Apply Neuber correction
            s_corr = solve_neuber_mmpds(s_lin, E_mod, Fty, n_exp)
            status = "PLASTIC (Neuber Corrected)"
        else:
            # Keep linear stress if safe
            s_corr = s_lin 
            status = "Elastic (Linear)"
            
        # Calculate Strain results
        e_tot = get_strain_ramberg(s_corr, E_mod, Fty, n_exp)
        e_elas = s_corr / E_mod
        e_plas = max(e_tot - e_elas, 0.0) 
        
        # Calculate stress reduction percentage and Margin of Safety against Ultimate Strength
        reduct = ((s_lin - s_corr)/s_lin)*100 if s_lin > 0 else 0 
        mos = (Ftu / s_corr) - 1 if s_corr > 0 else 999.9 
            
        res['stress'].append(s_corr)
        res['status'].append(status)
        res['reduct'].append(reduct)
        res['e_elas'].append(e_elas)
        res['e_plas'].append(e_plas)
        res['e_tot'].append(e_tot)
        res['mos'].append(mos)

    # Append results as new columns to the DataFrame
    df_out = df_calc.copy()
    df_out['Neuber_Stress'] = res['stress']
    df_out['MoS_Corrected'] = res['mos']
    df_out['Reduction_%'] = res['reduct']
    df_out['Elastic_Strain'] = res['e_elas']
    df_out['Plastic_Strain'] = res['e_plas']
    df_out['Total_Strain'] = res['e_tot']
    df_out['Status'] = res['status']
    
    gc.collect() # Trigger garbage collection to free RAM
    return df_out

