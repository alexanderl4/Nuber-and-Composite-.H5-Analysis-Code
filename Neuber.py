def clean_subtitle(text):
    """ Cleans byte strings (e.g., b'force2x') and removes unnecessary quotes """
    # If the text is in byte format, decode it to a normal string
    if isinstance(text, bytes): 
        text = text.decode('utf-8', errors='ignore')
    text = str(text).strip()
    
    # Remove the b'...' or b"..." wrapper usually generated by Nastran
    if (text.startswith("b'") and text.endswith("'")) or (text.startswith('b"') and text.endswith('"')):
        text = text[2:-1]
        
    # Remove any remaining single or double quotes
    text = text.replace("'", "").replace('"', '')
    return text.strip()

def safe_filename(text):
    """ Replaces illegal characters in a string to make it safe for saving as a file name (.png) """
    return re.sub(r'[^A-Za-z0-9_\-\.]', '_', text)

def parse_element_ranges(range_str):
    """ Converts a user input string like '1-500, 1001' into a Python list of integers """
    element_ids = set() # Use a set to automatically prevent duplicate IDs
    parts = range_str.split(',') # Split input by comma
    for part in parts:
        part = part.strip()
        if '-' in part: # If it's a range (e.g., 1-500)
            try:
                start_str, end_str = part.split('-')
                # Add all numbers from start to end into the set
                element_ids.update(range(int(start_str.strip()), int(end_str.strip()) + 1))
            except ValueError: 
                pass
        elif part.isdigit(): # If it's a single number
            element_ids.add(int(part))
    return list(element_ids) # Return as a standard list

