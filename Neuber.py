def parse_bdf_subtitles(bdf_filepath):
    """ Scans the .bdf file to extract custom Load Case names (Subtitles) defined in Patran """
    subcase_dict = {}
    if not bdf_filepath or not os.path.exists(bdf_filepath): 
        return subcase_dict
    
    print(f"    [+] Scanning Subtitles from: {os.path.basename(bdf_filepath)}...")
    current_subcase = None
    try:
        # Open and read the BDF text file line by line
        with open(bdf_filepath, 'r', errors='ignore') as f:
            for line in f:
                line = line.strip()
                if line.startswith("SUBCASE"): # Detect subcase block
                    try: 
                        current_subcase = int(line.split()[1]) # Extract the subcase ID number
                    except: 
                        pass
                elif line.startswith("SUBTITLE") and current_subcase is not None: # Detect subtitle
                    if "=" in line:
                        subtitle_text = line.split("=")[1].strip() # Extract text after '='
                        # Save the cleaned subtitle to the dictionary
                        subcase_dict[current_subcase] = f"Subcase {current_subcase}: {clean_subtitle(subtitle_text)}"
    except Exception: 
        pass
    
    return subcase_dict

def extract_h5_data(input_file, bdf_file=None):
    """ Main function to extract displacement and stress data from the Nastran .h5 database """
    print(f"    [+] Reading H5 Database: {os.path.basename(input_file)} ...")
    h5 = tables.open_file(input_file, mode="r") # Open the HDF5 file in read mode
    df_disp = pd.DataFrame() 
    
    # Run the BDF scanner to get user-defined names
    bdf_subtitles = parse_bdf_subtitles(bdf_file)
    domain_to_subcase = {} # Map internal DOMAIN_ID to actual SUBCASE ID
    h5_subtitles = {} # Fallback dictionary if BDF is missing but H5 has titles
    
    # --- Read the DOMAINS table (The Bridge) ---
    try:
        if hasattr(h5.root.NASTRAN.RESULT, 'DOMAINS'):
            domains_table = h5.root.NASTRAN.RESULT.DOMAINS.read()
            cols_dom = domains_table.dtype.names
            for row in domains_table:
                d_id = row['ID'] # Get internal DOMAIN ID
                sub_id = row['SUBCASE'] if 'SUBCASE' in cols_dom else d_id # Get actual SUBCASE ID
                domain_to_subcase[d_id] = sub_id
                
                # Check if Nastran stored the title directly inside the H5 file
                for t_col in ['SUBTITLE', 'TITLE', 'LABEL']:
                    if t_col in cols_dom:
                        val = row[t_col]
                        clean_val = clean_subtitle(val)
                        if clean_val:
                            h5_subtitles[sub_id] = f"Subcase {sub_id}: {clean_val}"
                            break
    except Exception: 
        pass

    def get_load_case_name(domain_id):
        """ Helper function to translate DOMAIN_ID to the correct Load Case string """
        real_subcase = domain_to_subcase.get(domain_id, domain_id)
        # Priority: 1. BDF Name -> 2. H5 Internal Name -> 3. Default "Subcase X"
        if real_subcase in bdf_subtitles: 
            return bdf_subtitles[real_subcase]
        elif real_subcase in h5_subtitles: 
            return h5_subtitles[real_subcase]
        else: 
            return f"Subcase {real_subcase}"

    # --- Extract Nodal Displacement Data ---
    try:
        data_disp = h5.root.NASTRAN.RESULT.NODAL.DISPLACEMENT.read()
        if 'DOMAIN_ID' in data_disp.dtype.names:
            # Map the names
            df_disp['Load_Case'] = [get_load_case_name(d_id) for d_id in data_disp['DOMAIN_ID']]
        for col in data_disp.dtype.names:
            if col in DEF_DISP: 
                df_disp[DEF_DISP[col]] = data_disp[col]
        if 'Tx' in df_disp.columns:
            # Calculate Total Magnitude (Vector Sum)
            df_disp['Total Mag'] = np.sqrt(df_disp['Tx']**2 + df_disp['Ty']**2 + df_disp['Tz']**2)
    except Exception: 
        pass 

    df_raw_list = []
    df_calc_list = []
    
    # --- Extract Elemental Stress Data (The Core Operation) ---
    try:
        stress_node = h5.root.NASTRAN.RESULT.ELEMENTAL.STRESS
        available_elements = list(stress_node._v_children.keys()) # Get all element types (QUAD4, HEXA, etc.)
        if not available_elements: 
            h5.close()
            return None, None, None

        # Loop through each element type found in the model
        for elem_name in available_elements:
            data = getattr(stress_node, elem_name).read()
            cols = data.dtype.names
            
            if 'EID' not in cols: 
                continue # Skip if no Element ID column
            
            df_temp_raw = pd.DataFrame()
            df_temp_raw['Element ID'] = data['EID']
            df_temp_raw['Element Type'] = elem_name
            df_temp_raw['Load_Case'] = [get_load_case_name(d_id) for d_id in data['DOMAIN_ID']] if 'DOMAIN_ID' in cols else "Subcase 1"
            
            vm_matrix = None
            dim_type = "Unknown"
            
            # Smart Detection: Identify the element's physics dimension based on column signatures
            if all(c in cols for c in ['X', 'Y', 'Z', 'TXY', 'TYZ', 'TZX']):
                # 3D Solid Elements (Hexa, Tetra, Penta)
                sx, sy, sz = data['X'], data['Y'], data['Z']
                txy, tyz, tzx = data['TXY'], data['TYZ'], data['TZX']
                vm_matrix = np.sqrt(0.5 * ((sx-sy)**2 + (sy-sz)**2 + (sz-sx)**2 + 6*(txy**2 + tyz**2 + tzx**2)))
                dim_type = "3D_Solid"
                
            elif all(c in cols for c in ['X', 'Y', 'TXY']):
                # 2D Shell Elements (Midplane only)
                sx, sy, txy = data['X'], data['Y'], data['TXY']
                vm_matrix = np.sqrt(sx**2 + sy**2 - sx*sy + 3*(txy**2))
                dim_type = "2D_Shell"
                
            elif all(c in cols for c in ['X1', 'Y1', 'TXY1', 'X2', 'Y2', 'TXY2']):
                # 2D Shell Elements (Top and Bottom surfaces)
                sx1, sy1, txy1 = data['X1'], data['Y1'], data['TXY1']
                vm1 = np.sqrt(sx1**2 + sy1**2 - sx1*sy1 + 3*(txy1**2)) # Top surface Von Mises
                
                sx2, sy2, txy2 = data['X2'], data['Y2'], data['TXY2']
                vm2 = np.sqrt(sx2**2 + sy2**2 - sx2*sy2 + 3*(txy2**2)) # Bottom surface Von Mises
                
                vm_matrix = np.maximum(vm1, vm2) # Take the worst (maximum) between Top and Bottom
                dim_type = "2D_Shell_TopBottom"
                
            elif all(c in cols for c in ['AX', 'MAX', 'MIN', 'XC', 'XD']):
                # 1D Bar Elements
                vm_matrix = np.maximum(np.abs(data['MAX']), np.abs(data['MIN']))
                dim_type = "1D_Bar"
                
            elif 'AXIAL' in cols:
                # 1D Rod Elements
                vm_matrix = np.abs(data['AXIAL'])
                dim_type = "1D_Line"
                
            elif 'X' in cols and 'Y' not in cols:
                # Other 1D line elements
                vm_matrix = np.abs(data['X'])
                dim_type = "1D_Line"
                
            else: 
                continue # Skip unsupported elements (e.g., Rigid, Spring, Bushing)

            # Unpack array matrices into individual columns (Center, Corner_1, Corner_2, etc.)
            for c in cols:
                if c in ['EID', 'DOMAIN_ID', 'TERM', 'GRID']: 
                    continue 
                arr = data[c]
                if len(arr.shape) > 1: # If it's a matrix (multiple nodes per element)
                    for i in range(arr.shape[1]): 
                        df_temp_raw[f"{c}_{'Center' if i==0 else f'Corner_{i}'}"] = arr[:, i]
                else: 
                    df_temp_raw[c] = arr # If it's a single value (center only)

            # Determine the maximum Von Mises stress for each element
            if len(vm_matrix.shape) > 1:
                vm_max = np.max(vm_matrix, axis=1) # Get highest stress among all corners/center
                for i in range(vm_matrix.shape[1]): 
                    df_temp_raw[f"VonMises_{'Center' if i==0 else f'Corner_{i}'}"] = vm_matrix[:, i]
            else:
                vm_max = vm_matrix 
                df_temp_raw["VonMises_Center"] = vm_max
            
            # Create a concise DataFrame for the actual calculation
            df_temp_calc = pd.DataFrame({
                'Load_Case': df_temp_raw['Load_Case'], 
                'Element ID': data['EID'],
                'Element Type': elem_name,
                'Dimension': dim_type,
                'Linear_VonMises_Max': vm_max
            })
            
            df_raw_list.append(df_temp_raw)
            df_calc_list.append(df_temp_calc)
            
        if not df_calc_list: 
            h5.close()
            return None, None, None
            
        # Combine all element types into one large master table
        df_raw = pd.concat(df_raw_list, ignore_index=True)
        df_calc = pd.concat(df_calc_list, ignore_index=True)
        
        # RAM Optimization: Convert repetitive string columns into Categorical format to save huge memory
        df_raw['Load_Case'] = df_raw['Load_Case'].astype('category')
        df_raw['Element Type'] = df_raw['Element Type'].astype('category')
        df_calc['Load_Case'] = df_calc['Load_Case'].astype('category')
        df_calc['Element Type'] = df_calc['Element Type'].astype('category')
        
        # Sort data by Load Case, and put the highest stress at the top (descending)
        df_calc = df_calc.sort_values(['Load_Case', 'Linear_VonMises_Max'], ascending=[True, False]).reset_index(drop=True)

    except Exception as e: 
        print(f"  -> Fatal error: {e}")
        h5.close()
        return None, None, None
        
    h5.close() # Always close the H5 file to prevent memory leaks
    return df_disp, df_raw, df_calc

def convert_stress_to_mpa(df_raw, df_calc, factor):
    """ Converts all stress values based on the user-selected unit conversion factor """
    if factor == 1.0: 
        return df_raw, df_calc
        
    df_r, df_c = df_raw.copy(), df_calc.copy()
    
    # Multiply every numerical column in raw data by the factor
    for col in df_r.columns:
        if df_r[col].dtype.kind == 'f': 
            df_r[col] = df_r[col] * factor
            
    # Multiply the calculation data by the factor
    df_c['Linear_VonMises_Max'] = df_c['Linear_VonMises_Max'] * factor
    
    return df_r, df_c

