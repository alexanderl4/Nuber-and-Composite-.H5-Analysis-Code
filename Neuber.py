def get_strain_ramberg(stress, E, Fty, n):
    """ Calculates the total strain using the Ramberg-Osgood material model """
    s = np.asarray(stress) 
    elastic = s / E # Calculate elastic strain (Hooke's Law)
    # Calculate plastic strain (only applies if stress is greater than 0)
    plastic = np.where(s > 0, 0.002 * ((s / Fty)**n), 0.0)
    return elastic + plastic # Total strain = Elastic + Plastic

def solve_neuber_mmpds(sigma_linear, E, Fty, n):
    """ Solves Neuber's rule to find the true local stress using Newton-Raphson method """
    energy_target = (sigma_linear**2) / E # Calculate the theoretical linear strain energy
    
    # Define the difference function that the solver needs to minimize to zero
    def func_diff(s_neu):
        if s_neu <= 0: 
            return -energy_target 
        e_tot = get_strain_ramberg(s_neu, E, Fty, n) # Get total strain for current guessed stress
        return (s_neu * e_tot) - energy_target # Difference between Neuber energy and Linear energy
        
    try: 
        # Use SciPy's Newton-Raphson optimizer to find the root (true stress)
        return optimize.newton(func_diff, x0=sigma_linear*0.9, maxiter=50, tol=SOLVER_TOL)
    except: 
        # If solver fails to converge, return the original linear stress as a fallback
        return sigma_linear 

