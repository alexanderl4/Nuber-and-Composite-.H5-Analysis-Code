def manual_neuber_input():
    """ Provides a quick command-line tool to calculate Neuber for a single stress value """
    print("\n" + "═"*55)
    print(" 🧮 MANUAL NEUBER CALCULATOR (SINGLE POINT)")
    print("═"*55)
    
    try:
        # Ask user for Material Inputs
        val_E   = float(input("  Young's Modulus (E) [MPa] : "))
        val_Fty = float(input("  Yield Strength (Fty) [MPa]: "))
        val_Ftu = float(input("  Ultimate Str (Ftu) [MPa]  : "))
        n_input = input("  Hardening (n) [Enter: 15] : ").strip()
        val_n   = float(n_input) if n_input else 15.0
        
        # Ask user for Unit Preferences
        print("\n  [Unit Selection]")
        print("  [1] Pascal (Pa)")
        print("  [2] Megapascal (MPa) - Default")
        print("  [3] Gigapascal (GPa)")
        print("  [4] PSI (Pounds per Square Inch)")
        unit_choice = input("  Select (1/2/3/4): ").strip()
        to_mpa_dict = {'1': 1e-6, '2': 1.0, '3': 1000.0, '4': 0.00689475729} 
        unit_names = {'1': 'Pa', '2': 'MPa', '3': 'GPa', '4': 'PSI'}
        conv_factor = to_mpa_dict.get(unit_choice, 1.0)
        stress_unit = unit_names.get(unit_choice, 'MPa')
        
        # Ask for the single stress value
        s_lin_input = float(input(f"\n  Enter Max Linear Stress [{stress_unit}]: "))
    except ValueError:
        print("  [!] Input must be a number. Program aborted.")
        return
    
    # Calculate Logic
    s_lin_mpa = s_lin_input * conv_factor
    is_plastic = s_lin_mpa > val_Fty
    
    if is_plastic:
        s_corr = solve_neuber_mmpds(s_lin_mpa, val_E, val_Fty, val_n)
        status = "PLASTIC (Neuber Corrected)"
    else:
        s_corr = s_lin_mpa
        status = "ELASTIC (Linear, No correction needed)"
        
    e_tot = get_strain_ramberg(s_corr, val_E, val_Fty, val_n)
    e_elas = s_corr / val_E
    e_plas = max(e_tot - e_elas, 0.0)
    reduct_pct = ((s_lin_mpa - s_corr) / s_lin_mpa) * 100 if s_lin_mpa > 0 else 0.0
    mos = (val_Ftu / s_corr) - 1 if s_corr > 0 else 999.9
    
    # Print Output Results
    print("\n" + "═"*55)
    print(" 📊 NEUBER ANALYSIS RESULTS")
    print("═"*55)
    print(f"  Status           : {status}")
    if stress_unit != 'MPa':
        print(f"  Linear Input     : {s_lin_input:,.2f} {stress_unit} --> ({s_lin_mpa:,.2f} MPa)")
    else:
        print(f"  Linear Input     : {s_lin_input:,.2f} MPa")
    print(f"  Actual Stress    : {s_corr:,.2f} MPa")
    print(f"  Stress Reduction : {reduct_pct:.2f} %") 
    print(f"  Total Strain     : {e_tot:.6f} mm/mm")
    print(f"  Margin of Safety : {mos:.2f} (Against Ultimate Strength)")
    print("═══════════════════════════════════════════════════════\n")

